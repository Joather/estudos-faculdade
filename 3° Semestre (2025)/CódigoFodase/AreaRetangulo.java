public class AreaRetangulo {

    public static void main(String[] args) {
        // Variaveis do retângulo
        int comprimento = 4;
        int largura = 6;
        int area;

        // Calculo da área
        area = comprimento * largura;

        // Imprimindo resultados
        System.out.println(
            "A base(Comprimento) do retângulo: " + comprimento + "cm"
        );
        System.out.println("A largura do retângulo: " + largura + "cm");
        System.out.println("A Área do retângulo é de " + area + "cm");
    }
}
